public abstract class Header {
    private int total;
    private int pageNo=1;
    private int limit = 20;

    protected Header(int limit, int pageNo, int total) {
        this.limit = limit;
        this.pageNo = pageNo;
        this.total = total;
    }

    public int getLimit() {
        return limit;
    }

    public void setLimit(int limit) {
        this.limit = limit;
    }

    public int getPageNo() {
        return pageNo;
    }

    public void setPageNo(int pageNo) {
        this.pageNo = pageNo;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
